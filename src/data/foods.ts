// Helper function to get foods by category
export function getFoodsByCategory(category: FoodItem["category"]): FoodItem[] {
  return foods.filter((f) => f.category === category);
}

// Helper function to get foods by FODMAP level
export function getFoodsByLevel(level: FoodItem["overallLevel"]): FoodItem[] {
  return foods.filter((f) => f.overallLevel === level);
}

// Helper function to search foods by name
export function searchFoods(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase();
  return foods.filter(
    (f) =>
      f.name.toLowerCase().includes(lowerQuery) ||
      f.category.toLowerCase().includes(lowerQuery)
  );
}

// Helper function to get low FODMAP alternatives for a food
export function getAlternatives(foodId: string): FoodItem[] {
  const food = foods.find((f) => f.id === foodId);
  if (!food?.alternatives) return [];
  return foods.filter((f) => food.alternatives?.includes(f.id));
}

